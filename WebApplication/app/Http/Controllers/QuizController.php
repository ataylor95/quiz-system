     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //Some simple validation, currently just require the two fields
        $this->validate($request, [
            'name' => 'required',
            'desc' => 'required'
        ]);

        //Create the new quiz
        Quiz::Create([
            'name' => $request['name'],
            'desc' => $request['desc'],
            'user_id' => auth()->user()->id
        ]);

        return redirect('/home');
    }

